function [z,T,P,rho,g,theta,sigma,delta,a] = AtmModel(H)
    gamma = 1.4;
    Re = 6356766;
    R = 287.0528;
    g0 = 9.806645;
    PSL = 101325;
    TSL = 288.15;
    rhoSL = 1.225;
    z = Re*H/(Re+H);
    g = g0*(Re/(Re+H))^2;

    if z>=0 && z<11000
        T = TSL-0.0065*z;
        P = PSL*((TSL-0.0065*z)/TSL)^(-g0/(R*-0.0065));
    end

    Pi = PSL*((TSL-0.0065*11000)/TSL)^(-g0/(R*-0.0065));
    if z>=11000 && z<20000
        T = 216.650; 
        P = Pi*exp(-g0*(z-11000)/(R*216.65));
    end

    Pi = Pi*exp(-g0*(20000-11000)/(R*216.65));
    if z>=20000 && z<32000
        T = 216.650+0.001*(z-20000);
        P = Pi*((216.65+0.001*(z-20000))/216.65)^(-g0/(R*0.001));
    end

    Pi = Pi*((216.65+0.001*(32000-20000))/216.65)^(-g0/(R*0.001));
    if z>=32000 && z<47000
        T = 228.65 +0.0028*(z-32000); 
        P = Pi*((228.65+0.0028*(z-32000))/228.65)^(-g0/(R*0.0028));
    end

    Pi = Pi*((228.65+0.0028*(47000-32000))/228.65)^(-g0/(R*0.0028));
    if z>=47000 && z<52000
        T = 270.65; 
        P = Pi*exp(-g0*(z-11000)/(R*270.65));
    end

    Pi = Pi*exp(-g0*(52000-11000)/(R*270.65));
    if z>=52000 && z<61000
        T = 270.65 + (-0.002) * (z-52000); 
        P = Pi*((270.65+(-0.002)*(z-52000))/270.65)^(-g0/(R*-0.002));
    end

    Pi = Pi*((270.65+(-0.002)*(61000-52000))/270.65)^(-g0/(R*-0.002));
    if z>=61000 && z<79000
        T = 252.65 + (-0.004) * (z-61000); 
        P = Pi*((252.65+(-0.004)*(z-61000))/252.65)^(-g0/(R*-0.004));
    end
    
    Pi = Pi*((252.65+(-0.004)*(79000-61000))/252.65)^(-g0/(R*-0.004));
    if z>=79000 && z<90000
        T = 180.65; 
        P = Pi*exp(-g0*(z-79000)/(R*180.65));
    end
    rho = P/(R*T);
    theta = T/TSL;
    sigma = rho/rhoSL;
    delta = P/PSL;
    a = sqrt(gamma*R*T);
end